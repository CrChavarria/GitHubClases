#sumar
def sumar (numero_1:int,numero_2:int):
    return numero_1 + numero_2
#resta
def resta (numero_1:int,numero_2:int):
    return numero_1 - numero_2
#divicion
def divicion (numero_1:int,numero_2:int):
    return numero_1 / numero_2
#multiplicacion
def multiplicacion (numero_1:int,numero_2:int):
    return numero_1 * numero_2
#exponencial
def exponencial (numero_1:int,numero_2:int):
    return numero_1 ** numero_2